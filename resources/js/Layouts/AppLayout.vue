        case 'connect':
        case 'info':
            infoToast.value = true;
            infoToastText.value = message;
            break;
        case 'success':
            successToast.value = true;
            successToastText.value = message;
            break;
        case 'danger':
            dangerToast.value = true;
            dangerToastText.value = message;
            break;
        case 'warning':
            warningToast.value = true;
            warningToastText.value = message;
            break;
