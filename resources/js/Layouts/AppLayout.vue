const message = (type, message) => {
    // Prevent triggering if already active or if type is invalid
    if (!['connect', 'success', 'danger', 'warning', 'info'].includes(type) || infoToast.value || successToast.value || dangerToast.value || warningToast.value) {
        console.warn('Toast already active or invalid type:', type);
        //return;
    }

    infoToast.value = false;
    successToast.value = false;
    dangerToast.value = false;
    warningToast.value = false;

    console.log('toast', type, message);
